package com.guo.pojo;

public class LineDetail {
    private Integer linedetailId;
    private Integer lineId;
    private Integer deliverspotId;
    private Integer lineindex;

    public Integer getLinedetailId() {
        return linedetailId;
    }

    public void setLinedetailId(Integer linedetailId) {
        this.linedetailId = linedetailId;
    }

    public Integer getLineId() {
        return lineId;
    }

    public void setLineId(Integer lineId) {
        this.lineId = lineId;
    }

    public Integer getDeliverspotId() {
        return deliverspotId;
    }

    public void setDeliverspotId(Integer deliverspotId) {
        this.deliverspotId = deliverspotId;
    }

    public Integer getLineindex() {
        return lineindex;
    }

    public void setLineindex(Integer lineindex) {
        this.lineindex = lineindex;
    }
}
